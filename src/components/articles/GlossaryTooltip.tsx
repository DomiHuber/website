'use client';

import { useState } from 'react';
import Link from 'next/link';
import { ExternalLink } from 'lucide-react';

interface GlossaryTooltipProps {
  term: string;
  slug: string;
  shortDefinition: string;
  children: React.ReactNode;
}

export default function GlossaryTooltip({ term, slug, shortDefinition, children }: GlossaryTooltipProps) {
  const [showTooltip, setShowTooltip] = useState(false);

  return (
    <span className="relative inline">
      <Link
        href={`/glossary/${slug}`}
        target="_blank"
        rel="noopener noreferrer"
        className="glossary-term text-primary font-medium border-b border-primary/40 border-dotted hover:border-primary hover:bg-primary/5 transition-all duration-200 cursor-help inline-flex items-baseline gap-0.5"
        onMouseEnter={() => setShowTooltip(true)}
        onMouseLeave={() => setShowTooltip(false)}
        onFocus={() => setShowTooltip(true)}
        onBlur={() => setShowTooltip(false)}
      >
        {children}
        <ExternalLink className="inline-block w-3 h-3 ml-0.5 opacity-40" />
      </Link>
      
      {showTooltip && (
        <div className="glossary-tooltip absolute w-80 mt-2 left-1/2 -translate-x-1/2 pointer-events-none" style={{ zIndex: 9999 }}>
          {/* Tooltip arrow */}
          <div className="absolute -top-2 left-1/2 -translate-x-1/2 w-3 h-3 bg-white border-l border-t border-gray-200 transform rotate-45" style={{ boxShadow: '-2px -2px 4px rgba(0,0,0,0.05)' }}></div>
          
          {/* Tooltip content */}
          <div className="relative bg-white rounded-xl border border-gray-200 p-4" style={{ boxShadow: '0 10px 40px rgba(0,0,0,0.15), 0 0 0 1px rgba(0,0,0,0.05)' }}>
            <div className="flex items-start justify-between gap-3 mb-2">
              <h4 className="font-bold text-base text-gray-900" style={{ fontWeight: 800 }}>
                {term}
              </h4>
              <span className="flex-shrink-0 px-2 py-0.5 text-xs font-semibold bg-primary/10 text-primary rounded-full" style={{ fontWeight: 600 }}>
                Glossary
              </span>
            </div>
            
            <p className="text-sm text-gray-600 leading-relaxed mb-3" style={{ fontWeight: 400 }}>
              {shortDefinition}
            </p>
            
            <div className="flex items-center gap-1.5 text-xs text-primary font-semibold" style={{ fontWeight: 600 }}>
              <ExternalLink className="w-3 h-3" />
              <span>Click to view full definition</span>
            </div>
          </div>
        </div>
      )}
    </span>
  );
}

